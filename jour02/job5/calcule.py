def check(n):
      
    # si le nombre est positif
    if n > 0:
        print("Positive")
          
    # si le nombre est négatif
    elif n < 0:
        print("Negative")
          
    # si le nombre est égal à zero
    else:
        print("Equal to zero")
          
check(5)
check(0)
check(-5)